vim.g.mapleader = " "
vim.keymap.set("n", "<leader>pv", vim.cmd.Ex)

vim.keymap.set("v", "J", ":m '>+1<CR>gv=gv")
vim.keymap.set("v", "K", ":m '<-2<CR>gv=gv")

vim.keymap.set("n", "J", "mzJ`z")
vim.keymap.set("n", "<C-d>", "<C-d>zz")
vim.keymap.set("n", "<C-u>", "<C-u>zz")
vim.keymap.set("n", "n", "nzzzv")
vim.keymap.set("n", "N", "Nzzzv")

-- move between split windows
vim.keymap.set("n", "<C-h>", "<C-w>h")
vim.keymap.set("n", "<C-j>", "<C-w>j")
vim.keymap.set("n", "<C-k>", "<C-w>k")
vim.keymap.set("n", "<C-l>", "<C-w>l")

-- paste and keep copy of data on the void register 
vim.keymap.set("x", "<leader>p", [["_dP]])

-- copy things on system register 
vim.keymap.set({ "n", "v" }, "<leader>y", [["+y]])
vim.keymap.set("n", "<leader>Y", [["+Y]])

vim.keymap.set({ "n", "v" }, "<leader>d", [["_d]])

vim.keymap.set("n", "<leader>f", vim.lsp.buf.format)

vim.keymap.set("n", "<C-k>", "<cmd>cnext<CR>zz")
vim.keymap.set("n", "<C-j>", "<cmd>cprev<CR>zz")
vim.keymap.set("n", "<leader>k", "<cmd>lnext<CR>zz")
vim.keymap.set("n", "<leader>j", "<cmd>lprev<CR>zz")

-- replace selected word in file
vim.keymap.set("n", "<leader>s", [[:%s/\<<C-r><C-w>\>/<C-r><C-w>/gI<Left><Left><Left>]])
vim.keymap.set("n", "<leader>x", "<cmd>!chmod +x %<CR>", { silent = true })


-- :so shortcut
vim.keymap.set("n", "<leader><leader>", function()
   vim.cmd("so")
end)

-- :NvimTreeToggle toggle file tree
vim.keymap.set("n", "<leader>e", function()
    vim.cmd("NvimTreeToggle")
end)

-- :NvimTreeFocus open tree if not open and focus 
vim.keymap.set("n", "<leader>r", function()
    vim.cmd("NvimTreeFocus")
end)

-- Shortcut to display errors in a floating window
vim.api.nvim_set_keymap('n', '<Leader>E', '<Cmd>lua vim.diagnostic.open_float(nil, {focusable=false})<CR>', { noremap = true, silent = true })

